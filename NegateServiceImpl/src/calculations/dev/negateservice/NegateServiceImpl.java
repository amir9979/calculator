
package calculations.dev.negateservice;

import calculations.dev.NegateRequest;
import calculations.dev.NegateResponse;

public class NegateServiceImpl
    implements NegateService
{


    public NegateResponse negate(NegateRequest param0) {
        NegateResponse response = new NegateResponse();
        response.setY(-param0.getX());
        return response;
    }

}
